			<h2 class="text-center">Different Fields In Engineering</h2>
		</div>
		<div class="container-fluid">
			 <article>
                        
<p>Engineering contains a large number of job opportunities and specialties. We’ve selected a list of specialties below. With each specialty, we look at the definition and nature of the work, the specialties employment trends, possibly career advancement opportunities, and hope that it is helpful for you in determining whether or not the career is right for you.
</p><br>
<u><h2>Aerospace Engineer</h2></u>
<p>Aerospace engineering is the study of the design, development, and production of air and spacecraft. This engineering discipline is often divided between those who pursue careers on the aeronautical side and those working on space craft. Both air and space vehicles contain complex subsystems that require specialists from many engineering groups such as electrical, mechanical, and computer engineering. Read More...
</p>
<u><h2>Agricultural Engineer</h2></u>
<p>Agricultural engineering is also known as biological engineering, and it covers subjects from aquaculture (raising food sources that thrive in water), to land farming and forestry. These engineers also develop biofuels, plan animal environments, and find better food processing methods. Often they work in offices, but they are also outdoors and traveling to worksites where they oversee equipment function in agricultural settings, and assure that government regulations are met. Read more...
</p>
<u><h2>Automotive Engineer</h2></u>
<p>Automotive engineering is one of the most exciting, challenging and rewarding careers. Whenever a customer drives a new vehicle off a dealership lot, he or she is taking with them the technical expertise of many engineers, but in particular, the automotive engineer. Automotive engineers research, design and develop vehicles and their subsystems. They work with sophisticated technologies to create products that thrill the senses and bring the freedom of mobility to the world. Read more...
</p>
<u><h2>Biomedical Engineer</h2></u>
<p>Biomedical engineers work with a combination of biology, medicine and engineering. They are trained to analyze and design solutions that will improve patient care. They are the professionals behind sophisticated medical equipment like MRIs and microscopic surgical machines. Biomedical engineers are also responsible for research and development of medical innovations like artificial organs and prosthesis. Read More...
</p>
<u><h2>Chemical Engineer</h2></u>
<p>Chemical engineers utilize their knowledge of the physical world to manipulate the interactions of individual atoms and molecules. Their talents are generally employed in the research and development of new materials and are critical to numerous fields including nanotechnology, energy storage, and computing. Often working alongside other engineers in interdisciplinary teams to solve humanity's greatest problems, chemical engineers are guaranteed to remain key leaders in securing our future prosperity whether on this planet or any other. Read More...
</p>
<u><h2>Civil Engineer</h2></u>
<p>Civil engineers specialize in road, bridge, buildings and water supply system design and construction. They supervise and direct construction teams and work with other engineers. These professionals ensure that every structure built is environmentally compliant and can withstand earthquakes and hurricanes. This is especially true in places where these natural calamities often strike. Read More...
</p>
<u><h2>Computer Engineer</h2></u>
<p>Computer Engineers develop and improve the software programs and hardware that make computers run. Computer Engineers may specialize in either software or hardware. From operating system software, such as Windows and Linux, to individual computer programs, such as Photoshop and Microsoft Office, Software Engineers turn piles of hardware into fully functional computers. Hardware Engineers develop the hardware of computers, including the motherboards, graphics and audio cards and drives that are later programmed by Software Engineers. Read More...
</p>
<u><h2>Drafting and Design Engineer</h2></u>
<p>Drafting and Design Engineering is an exciting career that allows the engineer to be involved in all stages of the design process, from conception to presentation of the finished plans. This career requires a working knowledge of drafting and design principles, material types and properties, and manufacturing processes. Read More...
</p>
<u><h2>Electrical Engineer</h2></u>
<p>Electrical engineers specialize in power supply and generation. They design, develop, test and supervise electrical equipment manufacturing. They have also been trained to handle responsibilities like wiring and lighting installations in buildings, automobiles and aircraft. What is great about being an electrical engineer is that the training is so extensive that graduates may land a job in many different industries such as construction, manufacturing and design. Read More...
</p>
<u><h2>Environmental Engineer</h2></u>
<p>Environmental engineers use science and engineering principles to protect and improve the environment. The quality of air, water, and soil is their primary focus. They seek solutions to water-borne diseases, wastewater management, and air pollution. They work to improve recycling, waste disposal, and industrial hygiene. They analyze soil and water samples. They understand the law as it applies to protecting the environment. Read more...
</p>
<u><h2>Geological Engineer</h2></u>
<p>Geological engineering involves geology,civil engineering, and fields such as mining, forestry and geography. These engineers apply earth sciences to human problems. Specialty areas include geotechnical site studies of rock and soil slope stability for projects; environmental studies and planning for construction sites; groundwater studies; hazard investigations; and finding fossil fuel and mineral deposits. Read more...
</p>
<u><h2>Marine Engineer</h2></u>
<p>Marine Engineers are responsible for the design and construction of seagoing vessels and structures, focusing primarily on their internal systems. Simply put, they design the onboard electrical, environmental and propulsion systems aboard everything from oil platforms to cruise ships. Read more...
</p>
<u><h2>Mechanical Engineer</h2></u>
<p>Mechanical engineering is the study of motion, energy and force. The mechanical engineer seeks to control these elements by using a combination of material, human and economic resources to develop mechanical solutions that help satisfy the needs and wants of society. Read more...
</p>
<u><h2>Petroleum Engineer</h2></u>
<p>Petroleum engineers specialize in designing and developing technology and methods for digging the earth’s surface to extract oil and gas. They find means to obtain either natural gas or crude oil from the ground. In addition, petroleum engineers explore and discover new techniques to extract oil and gas from older wells all over the world. Read More...
</p>
<u><h2>Software Engineer</h2></u>
<p>Software engineers are specialists who are in charge of the testing, design, development and maintenance of computer software for business and personal use. They apply the principles of mathematics, engineering and computer science in creating managing software. These professionals are responsible for programming computers to make them operate as they should.
</p>
